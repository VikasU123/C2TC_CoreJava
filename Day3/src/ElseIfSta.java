
public class ElseIfSta {

	public static void main(String[] args) {
		String lang = "Java";
		if(lang== "python") {
			System.out.println("language is python");
		}else if(lang=="C++") {
			System.out.println("lang is C++");
		}else if(lang=="java") {
			System.out.println("lang is java");
		}else {
			System.out.println(lang);
		}

	}

}
